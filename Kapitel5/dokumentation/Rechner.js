/**
 * Ein kleiner Taschenrechner
 *
 * @class Rechner
 * @constructor
 */
function Rechner() {
    /**
     * Diese Methode addiert zwei Zahlen.
     *
     * @method addiere
     * @param {Number} x Zahl 1
     * @param {Number} y Zahl 2
     * @return {Number} Liefert das Ergebnis der Addition von x und y.
     */
    this.addiere = function(x,y) {
        return x + y;
    }
}
